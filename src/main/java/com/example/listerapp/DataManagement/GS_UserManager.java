package com.example.listerapp.DataManagement;

public class GS_UserManager {
    public String user_name,user_age, user_gender, user_birthday, user_address, user_username, user_password;
    public GS_UserManager(String name, String age, String gender, String birthday, String address, String username, String password){
        this.user_name = name;
        this.user_age = age;
        this.user_gender = gender;
        this.user_birthday = birthday;
        this.user_address = address;
        this.user_username = username;
        this.user_password = password;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_age() {
        return user_age;
    }

    public void setUser_age(String user_age) {
        this.user_age = user_age;
    }

    public String getUser_gender() {
        return user_gender;
    }

    public void setUser_gender(String user_gender) {
        this.user_gender = user_gender;
    }

    public String getUser_birthday() {
        return user_birthday;
    }

    public void setUser_birthday(String user_birthday) {
        this.user_birthday = user_birthday;
    }

    public String getUser_address() {
        return user_address;
    }

    public void setUser_address(String user_address) {
        this.user_address = user_address;
    }

    public String getUser_username() {
        return user_username;
    }

    public void setUser_username(String user_username) {
        this.user_username = user_username;
    }

    public String getUser_password() {
        return user_password;
    }

    public void setUser_password(String user_password) {
        this.user_password = user_password;
    }
}
